// Linguagem pictográfica: é uma forma de comunicação visual que utiliza símbolos, ícones e imagens para representar conceitos, ideias ou informações. Ela é usada para transmitir mensagens de forma rápida e clara, sem a necessidade de palavras ou texto. A linguagem pictográfica é especialmente útil para pessoas que têm dificuldades de leitura ou para situações em que a comunicação verbal não é possível.   

// Símbolos usados em Domain Storytelling:

// Ator:
// Representa quem executa uma ação no processo.
// Exemplo: cliente, atendente, administrador.

// Objeto de trabalho:
// Representa aquilo que é manipulado durante o processo.
// Exemplo: pedido, cadastro, pagamento, produto.

// Atividade:
// Representa uma ação realizada por um ator.
// Exemplo: criar pedido, validar pagamento, enviar confirmação.

// Evento:
// Representa algo que aconteceu e pode iniciar ou alterar um fluxo.
// Exemplo: pagamento aprovado, pedido cancelado, cliente cadastrado.

// Sistema:
// Representa um software ou sistema que participa do processo.
// Exemplo: sistema de vendas, gateway de pagamento, banco de dados.

// Regra de negócio:
// Representa uma condição ou regra que precisa ser respeitada.
// Exemplo: pedido só pode ser enviado após pagamento confirmado.

// Relação ou fluxo:
// Representa a sequência de interação entre atores, objetos e atividades.
// Exemplo: cliente faz pedido -> sistema registra pedido -> atendente confirma pedido.

// Linguagem ubíqua: é a linguagem comum usada por todas as pessoas envolvidas no negócio, como clientes, especialistas do domínio e desenvolvedores.
// Ela reúne os termos, expressões e conceitos da regra de negócio, ajudando todos a se comunicarem com clareza e sem ambiguidades.
